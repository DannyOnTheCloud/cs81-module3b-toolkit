//This function multiplies the input by 2 
function double(num) {
  return num * 2;
}

// This function squares a number
function square(num) {
  return num * num;
}

//This function checks if the number is even
function isEven(num) {
  return num % 2 === 0;
}

// This function checks if the number is odd
function isOdd(num) {
  return num % 2 !== 0;
}

// This function multiplies two numbers
function multiply(a, b) {
  return a * b;
}

